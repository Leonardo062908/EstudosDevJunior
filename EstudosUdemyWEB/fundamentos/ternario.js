//Aprendendo sobre operadores ternários em JavaScript
const resultado = (nota) => (nota >= 7 ? "Aprovado" : "Reprovado"); // Função que usa operador ternário para determinar se a nota é suficiente para aprovação
console.log(resultado(7.5)); //Exibe 'Aprovado' para nota 7.5
console.log(resultado(6.3)); //Exibe 'Reprovado' para nota 6.3
